public class Coordinate {
    private UnsignedInteger x;
    private UnsignedInteger y;

    public Coordinate(UnsignedInteger x, UnsignedInteger y) {
        this.x = x;
        this.y = y;
    }

    public UnsignedInteger getX() {
        return x;
    }

    public UnsignedInteger getY() {
        return y;
    }
}
